const SearchIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'SearchIntent';
    },
    async handle(handlerInput) {
        const product = handlerInput.requestEnvelope.request.intent.slots.product.value;
        let response;

        try {
            response = await searchProduct(product);
        } catch (error) {
            console.error(`Error fetching product data: ${error}`);
            response = `I couldn't fetch the product information right now. Please try again later.`;
        }

        const speechText = response;

        return handlerInput.responseBuilder
            .speak(speechText)
            .reprompt()
            .getResponse();
    }
};

async function searchProduct(product) {
    const response = await axios.get(`https://amazon-product-search2.p.rapidapi.com/product/`, {
        params: { query: product },
        headers: { 
            'Authorization': `Bearer ${apiKey}`,
            'X-RapidAPI-Host': 'amazon-product-search2.p.rapidapi.com',
            'X-RapidAPI-Key': apiKey 
        }
    });

    if (response.data && response.data.items.length > 0) {
        return `I found these products for ${product}: ${response.data.items.map(p => p.name).join(', ')}.`;
    }
    return `I couldn't find any products for ${product}.`;
}

